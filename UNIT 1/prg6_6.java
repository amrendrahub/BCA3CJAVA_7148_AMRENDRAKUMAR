import java.util.Scanner;

 public class prg6_6{
    
		public static void main(String[] args){
		Scanner scn = new Scanner(System.in);
		
		System.out.print("enter your number:");
		int a = scn.nextInt();
		
		int i=0;
		do{
		 System.out.println(i);
		 i++;
		}while(i<=a);
		
		}
 
 } 
